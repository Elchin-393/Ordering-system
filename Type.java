package McDonaldsMenu;

public class Type {
	
	 private String burgers;	
	 private String snacks;
	 private String drinks;
	 private String sauces;
	 private String hotDrinks;
	 private String happyMeal;
	 private String iceCreamAndMilkshakes;
     private String Desserts;
	   
	   
   public Type() {
		super();
	}
   
   public String getBurgers() {
		return burgers;
	}
	public void setBurgers(String burgers) {
		this.burgers = burgers;
	}
   
   public String getDesserts() {
	return Desserts;
   }
   public void setDesserts(String desserts) {
	Desserts = desserts;
   }
   
   public String getSnacks() {
	return snacks;
   }
   public void setSnacks(String snacks) {
	this.snacks = snacks;
   }
   public String getDrinks() {
	return drinks;
   }
   public void setDrinks(String drinks) {
	this.drinks = drinks;
   }
   public String getSauces() {
	return sauces;
   }
   public void setSauces(String sauces) {
	this.sauces = sauces;
   }
   public String getHotDrinks() {
  	return hotDrinks;
   }
   public void setHotDrinks(String hotDrinks) {
	this.hotDrinks = hotDrinks;
   }
   public String getHappyMeal() {
	return happyMeal;
   }
   public void setHappyMeal(String happyMeal) {
	this.happyMeal = happyMeal;
   }
   public String getIceCreamAndMilkshakes() {
	return iceCreamAndMilkshakes;
   }
   public void setIceCreamAndMilkshakes(String iceCreamAndMilkshakes) {
	this.iceCreamAndMilkshakes = iceCreamAndMilkshakes;
}
   
   
}
