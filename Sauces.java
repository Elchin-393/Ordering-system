package McDonaldsMenu;

public class Sauces{	
	
	private String barbecueSauce;
	private String Ketchup;
	private String CheeseSauce;
	private String MustardSauce;
	private String SalsaSauce;
	private String Mayonnaise;
	private String SweetSauce;
	
	private double SaucePrice;
	
	public Sauces() {
		super();
		this.SaucePrice = 0.70;
		
	}

	public String getBarbecueSauce() {
		return barbecueSauce;
	}

	public void setBarbecueSauce(String barbecueSauce) {
		this.barbecueSauce = barbecueSauce;
	}


	public String getKetchup() {
		return "Ketchup";
	}

	public void setKetchup(String ketchup) {
		Ketchup = ketchup;
	}

	public String getCheeseSauce() {
		return CheeseSauce;
	}

	public void setCheeseSauce(String cheeseSauce) {
		CheeseSauce = cheeseSauce;
	}

	public String getMustardSauce() {
		return MustardSauce;
	}

	public void setMustardSauce(String mustardSauce) {
		MustardSauce = mustardSauce;
	}

	public String getSalsaSauce() {
		return SalsaSauce;
	}

	public void setSalsaSauce(String salsaSauce) {
		SalsaSauce = salsaSauce;
	}

	public String getMayonnaise() {
		return Mayonnaise;
	}

	public void setMayonnaise(String mayonnaise) {
		Mayonnaise = mayonnaise;
	}

	public String getSweetSauce() {
		return SweetSauce;
	}

	public void setSweetSauce(String sweetSauce) {
		SweetSauce = sweetSauce;
	}

	public double getSaucePrice() {
		return SaucePrice;
	}

	public void setSaucePrice(double saucePrice) {
		SaucePrice = saucePrice;
	}
	
	
	

	
}

