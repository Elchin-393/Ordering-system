package McDonaldsMenu;

public class HappyMeal {
	
	private String HappyMealWithCheeseBurger;
	private String HappyMealWithMcNuggets;
	private String HappyMealWithChickenBurger;
	private String HappyMealWithHamBurger;
	
	private double HappyMealsPrice;

	public HappyMeal() {
		super();
		this.HappyMealsPrice = 4.80;
	}

	public double getHappyMealsPrice() {
		return HappyMealsPrice;
	}

	public void setHappyMealsPrice(double happyMealsPrice) {
		HappyMealsPrice = happyMealsPrice;
	}

	public String getHappyMealWithCheeseBurger() {
		return HappyMealWithCheeseBurger;
	}

	public void setHappyMealWithCheeseBurger(String happyMealWithCheeseBurger) {
		HappyMealWithCheeseBurger = happyMealWithCheeseBurger;
	}

	public String getHappyMealWithMcNuggets() {
		return HappyMealWithMcNuggets;
	}

	public void setHappyMealWithMcNuggets(String happyMealWithMcNuggets) {
		HappyMealWithMcNuggets = happyMealWithMcNuggets;
	}

	public String getHappyMealWithChickenBurger() {
		return HappyMealWithChickenBurger;
	}

	public void setHappyMealWithChickenBurger(String happyMealWithChickenBurger) {
		HappyMealWithChickenBurger = happyMealWithChickenBurger;
	}

	public String getHappyMealWithHamBurger() {
		return HappyMealWithHamBurger;
	}

	public void setHappyMealWithHamBurger(String happyMealWithHamBurger) {
		HappyMealWithHamBurger = happyMealWithHamBurger;
	}
	

}
